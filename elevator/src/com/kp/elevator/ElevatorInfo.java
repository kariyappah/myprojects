package com.kp.elevator;

public class ElevatorInfo {

    private String elevatorType;

    private int elevatorId;

    private String elevatorInfo;

    private int floorsCount;

    public String getElevatorType() {
        return elevatorType;
    }

    public void setElevatorType(String elevatorType) {
        this.elevatorType = elevatorType;
    }

    public int getElevatorId() {
        return elevatorId;
    }

    public void setElevatorId(int elevatorId) {
        this.elevatorId = elevatorId;
    }

    public String getElevatorInfo() {
        return elevatorInfo;
    }

    public void setElevatorInfo(String elevatorInfo) {
        this.elevatorInfo = elevatorInfo;
    }

    public int getFloorsCount() {
        return floorsCount;
    }

    public void setFloorsCount(int floorsCount) {
        this.floorsCount = floorsCount;
    }
}
